class User {
  final String roleType;
  final String name;
  final String address;
  final String email;
  final String mobileNumber;
  final DateTime date;
  final String gender;
  final String password;

  User({
    required this.roleType,
    required this.name,
    required this.address,
    required this.email,
    required this.mobileNumber,
    required this.date,
    required this.gender,
    required this.password,
  });
}
